// ── Checkbox prompt ───────────────────────────────────────────────────────────
// items: Array<{ label: string, hint?: string }>
// defaultSelected: Set<number> — indices checked initially (default: all)
// Returns Promise<Set<number>>

function checkboxPrompt(items, heading, defaultSelected) {
  return new Promise((resolve) => {
    const selected = defaultSelected ?? new Set(items.map((_, i) => i));
    let cursor = 0;
    const LABEL_W = Math.max(...items.map((it) => it.label.length)) + 2;

    function renderItem(i) {
      const checked = selected.has(i) ? A.green("◉") : A.dim("○");
      const label = items[i].label.padEnd(LABEL_W);
      const hint = items[i].hint ? A.dim(items[i].hint) : "";
      const row = `  ${checked} ${label} ${hint}`;
      return i === cursor ? A.cyan(A.bold(`  ${checked} ${items[i].label.padEnd(LABEL_W)}`) + ` ${A.dim(items[i].hint ?? "")}`) : row;
    }

    write("\n");
    write(`  ${A.bold(heading)}\n`);
    write(`  ${A.dim("↑↓ navigate   space toggle   a select all   enter confirm")}\n\n`);
    for (let i = 0; i < items.length; i++) write(renderItem(i) + "\n");
    write("\n");

    function redraw() {
      write(A.up(items.length + 1));
      for (let i = 0; i < items.length; i++) write("\r" + A.clearLine + renderItem(i) + "\n");
      write("\n");
    }

    process.stdin.setRawMode(true);
    process.stdin.resume();
    process.stdin.setEncoding("utf8");
    write(A.hideCursor);

    function cleanup() {
      process.stdin.setRawMode(false);
      process.stdin.pause();
      write(A.showCursor);
    }

    process.stdin.on("data", function handler(key) {
      if (key === "\x03") { cleanup(); process.exit(0); }

      if (key === "\x1b[A" || key === "k") {
        cursor = (cursor - 1 + items.length) % items.length;
        redraw();
      } else if (key === "\x1b[B" || key === "j") {
        cursor = (cursor + 1) % items.length;
        redraw();
      } else if (key === " ") {
        if (selected.has(cursor)) selected.delete(cursor);
        else selected.add(cursor);
        redraw();
      } else if (key === "a" || key === "A") {
        if (selected.size === items.length) selected.clear();
        else items.forEach((_, i) => selected.add(i));
        redraw();
      } else if (key === "\r" || key === "\n") {
        cleanup();
        process.stdin.removeListener("data", handler);
        resolve(selected);
      }
    });

    process.on("exit", () => write(A.showCursor));
  });
}

// ── Radio prompt ──────────────────────────────────────────────────────────────
// Returns Promise<number>

function radioPrompt(options, heading) {
  return new Promise((resolve) => {
    let cursor = 0;

    function renderItem(i) {
      const dot = i === cursor ? A.green("◉") : A.dim("○");
      const text = `  ${dot} ${options[i].label}  ${A.dim(options[i].hint ?? "")}`;
      return i === cursor ? A.cyan(text) : text;
    }

    write("\n");
    write(`  ${A.bold(heading)}\n\n`);
    for (let i = 0; i < options.length; i++) write(renderItem(i) + "\n");
    write("\n");

    function redraw() {
      write(A.up(options.length + 1));
      for (let i = 0; i < options.length; i++) write("\r" + A.clearLine + renderItem(i) + "\n");
      write("\n");
    }

    process.stdin.setRawMode(true);
    process.stdin.resume();
    process.stdin.setEncoding("utf8");
    write(A.hideCursor);

    function cleanup() {
      process.stdin.setRawMode(false);
      process.stdin.pause();
      write(A.showCursor);
    }

    process.stdin.on("data", function handler(key) {
      if (key === "\x03") { cleanup(); process.exit(0); }
      if (key === "\x1b[A" || key === "k") { cursor = (cursor - 1 + options.length) % options.length; redraw(); }
      else if (key === "\x1b[B" || key === "j") { cursor = (cursor + 1) % options.length; redraw(); }
      else if (key === "\r" || key === "\n") {
        cleanup();
        process.stdin.removeListener("data", handler);
        resolve(cursor);
      }
    });

    process.on("exit", () => write(A.showCursor));
  });
}

// ── Skill loader ──────────────────────────────────────────────────────────────

async function loadSkills() {
  const files = (await readdir(SKILLS_DIR)).filter((f) => f.endsWith(".md"));
  const skills = await Promise.all(
    files.map(async (file) => {
      const raw = await readFile(join(SKILLS_DIR, file), "utf8");
      const { frontmatter, body } = parseFrontmatter(raw);
      return {
        slug: file.replace(/\.md$/, ""),
        name: String(frontmatter.name ?? file.replace(/\.md$/, "")),
        description: String(frontmatter.description ?? ""),
        globs: Array.isArray(frontmatter.globs) ? frontmatter.globs : [],
        body: body.trim(),
      };
    }),
  );
  return skills.sort((a, b) => a.slug.localeCompare(b.slug));
}

// ── Arg parser ────────────────────────────────────────────────────────────────

function parseArgs(argv) {
  const out = { all: false, skills: null, agents: null, scope: null };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === "--all" || a === "-y") out.all = true;
    else if (a.startsWith("--skills=")) out.skills = a.slice(9).split(",").map((s) => s.trim());
    else if (a.startsWith("--agents=")) out.agents = a.slice(9).split(",").map((s) => s.trim());
    else if (a.startsWith("--scope=")) out.scope = a.slice(8).trim();
    else if (a === "--scope" && argv[i + 1]) out.scope = argv[++i];
  }
  return out;
}

// ── Top-level menu items (what the user sees) ─────────────────────────────────
// Each item resolves to one or more skill slugs. Items with `hasSidePicker`
// show a follow-up radio when selected to choose native / JS / both.

const MENU_ITEMS = [
  {
    label: "nitropush-cli",
    hint: "CLI usage — login, release upload, rollouts, CI patterns",
    slugs: ["nitropush-cli"],
  },
  {
    label: "nitropush-integration",
    hint: "End-to-end app integration — native wiring + JS/TS SDK",
    slugs: ["nitropush-integration", "nitropush-rn-native", "nitropush-rn-js"],
  },
];

// ── Main ──────────────────────────────────────────────────────────────────────

async function main() {
  const cliArgs = parseArgs(process.argv.slice(2).filter((a) => a !== "init"));
  const skills = await loadSkills();
  const isTTY = process.stdin.isTTY;

  if (!isTTY && !cliArgs.all && (!cliArgs.skills || !cliArgs.agents)) {
    console.error(
      "Non-interactive: pass --all or --skills=<slugs> --agents=<ids> --scope=project|device",
    );
    process.exit(2);
  }

  write(`\n  ${A.bold("NitroPush Agent Skill Installer")}\n`);
  write(`  ${"─".repeat(33)}\n`);

  // ── 1. Which skills? ─────────────────────────────────────────────────────

  let selectedSkills;

  if (cliArgs.skills) {
    selectedSkills = cliArgs.skills.map((s) => skills.find((sk) => sk.slug === s)).filter(Boolean);
    if (!selectedSkills.length) { console.error(`No matching skills: ${cliArgs.skills.join(", ")}`); process.exit(1); }
  } else if (cliArgs.all) {
    selectedSkills = skills;
  } else {
    // Show the 2-item menu
    const menuIndices = await checkboxPrompt(
      MENU_ITEMS.map((m) => ({ label: m.label, hint: m.hint })),
      "Select skills to install",
    );
    if (!menuIndices.size) { write(`\n  ${A.dim("Nothing selected. Exiting.")}\n\n`); process.exit(0); }

    // Resolve slugs from selected menu items
    const slugs = [];
    for (const idx of [...menuIndices].sort((a, b) => a - b)) {
      slugs.push(...MENU_ITEMS[idx].slugs);
    }

    // Deduplicate and preserve order
    const seen = new Set();
    const uniqueSlugs = slugs.filter((s) => seen.has(s) ? false : seen.add(s));
    selectedSkills = uniqueSlugs.map((s) => skills.find((sk) => sk.slug === s)).filter(Boolean);
  }

  // ── 2. Which AI tools? ───────────────────────────────────────────────────

  let selectedAgents;

  if (cliArgs.agents) {
    selectedAgents = cliArgs.agents.map((id) => AGENTS.find((a) => a.id === id)).filter(Boolean);
    if (!selectedAgents.length) { console.error(`No matching agents: ${cliArgs.agents.join(", ")}`); process.exit(1); }
  } else if (cliArgs.all) {
    selectedAgents = AGENTS;
  } else {
    // Default: only Claude Code pre-selected
    const defaultSel = new Set([0]);
    const indices = await checkboxPrompt(
      AGENTS.map((a) => ({ label: a.label, hint: a.hint })),
      "Select AI tools to install for",
      defaultSel,
    );
    if (!indices.size) { write(`\n  ${A.dim("Nothing selected. Exiting.")}\n\n`); process.exit(0); }
    selectedAgents = [...indices].sort((a, b) => a - b).map((i) => AGENTS[i]);
  }

  // ── 3. Scope (only if Claude Code selected) ──────────────────────────────

  let isDevice = false;
  const needsScope = selectedAgents.some((a) => a.supportsDeviceScope);

  if (needsScope) {
    if (cliArgs.scope) {
      isDevice = cliArgs.scope === "device";
    } else {
      const choice = await radioPrompt(
        [
          { label: "project", hint: ".claude/skills/ in the current directory" },
          { label: "device ", hint: "~/.claude/skills/ (all projects on this machine)" },
        ],
        "Claude Code install scope",
      );
      isDevice = choice === 1;
    }
  }

  // ── 4. Write files ───────────────────────────────────────────────────────

  const projectBase = process.cwd();

  write("\n");

  for (const agent of selectedAgents) {
    if (agent.mode === "per-skill") {
      for (const skill of selectedSkills) {
        const base = (agent.supportsDeviceScope && isDevice) ? homedir() : projectBase;
        const filePath = agent.pathFn(skill.slug, base);
        await mkdir(dirname(filePath), { recursive: true });
        await writeFile(filePath, agent.render(skill), "utf8");
        const display = filePath.replace(homedir(), "~").replace(process.cwd() + "/", "");
        write(`  ${A.green("✔")}  ${A.dim(`[${agent.label}]`)}  ${display}\n`);
      }
    } else {
      // combined — write one file with all selected skills
      const base = projectBase;
      const filePath = agent.pathFn(null, base);
      await mkdir(dirname(filePath), { recursive: true });
      await writeFile(filePath, agent.renderCombined(selectedSkills), "utf8");
      const display = filePath.replace(process.cwd() + "/", "");
      write(`  ${A.green("✔")}  ${A.dim(`[${agent.label}]`)}  ${display}\n`);
    }
  }

  const skillNoun = selectedSkills.length === 1 ? "skill" : "skills";
  const agentNoun = selectedAgents.length === 1 ? "tool" : "tools";
  write(`\n  ${A.bold(`Done — ${selectedSkills.length} ${skillNoun} × ${selectedAgents.length} ${agentNoun} installed.`)}\n\n`);
}

// ── Frontmatter parser ────────────────────────────────────────────────────────

function parseFrontmatter(raw) {
  if (!raw.startsWith("---")) return { frontmatter: {}, body: raw };
  const end = raw.indexOf("\n---", 3);
  if (end === -1) return { frontmatter: {}, body: raw };
  const yamlText = raw.slice(3, end).replace(/^\n/, "");
  const body = raw.slice(end + 4).replace(/^\n/, "");
  return { frontmatter: parseYaml(yamlText), body };
}

function parseYaml(text) {
  const out = {};
  const lines = text.split("\n");
  let i = 0;
  while (i < lines.length) {
    const line = lines[i];
    if (!line.trim() || line.trimStart().startsWith("#")) { i++; continue; }
    const m = line.match(/^([A-Za-z_][A-Za-z0-9_]*):\s*(.*)$/);
    if (!m) { i++; continue; }
    const [, key, rest] = m;
    if (rest === "" || rest === "|") {
      const list = [];
      i++;
      while (i < lines.length && /^\s*-\s+/.test(lines[i])) {
        list.push(lines[i].replace(/^\s*-\s+/, "").trim());
        i++;
      }
      out[key] = list.length ? list : "";
      continue;
    }
    out[key] = coerce(rest.trim());
    i++;
  }
  return out;
}

function coerce(v) {
  if (v === "true") return true;
  if (v === "false") return false;
  if (/^-?\d+$/.test(v)) return Number(v);
  if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'")))
    return v.slice(1, -1);
  return v;
}

main().catch((err) => {
  write(A.showCursor);
  console.error(`\n  Error: ${err.message}\n`);
  process.exit(1);
});
